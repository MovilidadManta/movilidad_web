                        //initialise headline animation
                        animateHeadline($('.av-heading'));
                    }
                    //Single Letters
                    function singleLetters($words) {
                        $words.each(function() {
                            var word = $(this),
                                letters = word.text().split(''),
                                selected = word.hasClass('is-show');
                            var newLetters = letters.join('');
                            word.html(newLetters).css('opacity', 1);
                        });
                    }
                    //Animate Headline
                    function animateHeadline($headlines) {
                        var duration = animationDelay;
                        $headlines.each(function() {
                            var headline = $(this);
                            //trigger animation
                            setTimeout(function() {
                                hideWord(headline.find('.is-show').eq(0))
                            }, duration);
                        });
                    }
                    //Hide Word
                    function hideWord($word) {
                        var nextWord = takeNext($word);
                        switchWord($word, nextWord);
                        setTimeout(function() {
                            hideWord(nextWord)
                        }, animationDelay);
                    }
                    //Hide Letter
                    function hideLetter($letter, $word, $bool, $duration) {
                        $letter.removeClass('in').addClass('out');

                        if (!$letter.is(':last-child')) {
                            setTimeout(function() {
                                hideLetter($letter.next(), $word, $bool, $duration);
                            }, $duration);
                        } else if ($bool) {
                            setTimeout(function() {
                                hideWord(takeNext($word))
                            }, animationDelay);
                        }

                        if ($letter.is(':last-child') && $('html').hasClass('no-csstransitions')) {
                            var nextWord = takeNext($word);
                            switchWord($word, nextWord);
                        }
                    }
                    //Take Next
                    function takeNext($word) {
                        return (!$word.is(':last-child')) ? $word.next() : $word.parent().children().eq(0);
                    }
                    //Take Prev
                    function takePrev($word) {
                        return (!$word.is(':first-child')) ? $word.prev() : $word.parent().children()
                            .last();
                    }
                    //Switch Word
                    function switchWord($oldWord, $newWord) {
                        $oldWord.removeClass('is-show').addClass('is-hide');
                        $newWord.removeClass('is-hide').addClass('is-show');
                    }
